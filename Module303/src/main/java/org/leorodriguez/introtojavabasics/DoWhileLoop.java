package org.leorodriguez.introtojavabasics;
//You can always use a while loop in place of a for loop
//You CAN'T always use a for loop in place of a while loop

public class DoWhileLoop {
    public static void main(String[] args) {
        int i =1;
        do{
            System.out.println(i);
            i++;
        } while (i <=0);
    }
}
